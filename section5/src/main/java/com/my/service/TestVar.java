package section5.src.main.java.com.my.service;

public class TestVar {
    public static void main(String[] args) {
        // x 是變數名稱, 0 是賦予該變數的數值
        int x = 0;
        System.out.println("x = " + x);
        
        // 重新賦予該變數新的數值
        x = 1;
        System.out.println("x = " + x);
    }    
}
